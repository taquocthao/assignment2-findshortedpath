
public class AdjacentNode {
	private int id;
	private int weighted;
	
	public AdjacentNode(int id, int weighted) {
		this.id = id;
		this.weighted = weighted;
	}
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getWeighted() {
		return weighted;
	}
	public void setWeighted(int weighted) {
		this.weighted = weighted;
	}
	
	
	
}
